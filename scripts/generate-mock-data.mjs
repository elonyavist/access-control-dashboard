// Dev-only generator for the demo dataset. Run with: npm run generate:data
// Output is committed as src/features/events/mockData.ts, so faker stays a devDependency
// and never reaches the production bundle.
import { faker } from '@faker-js/faker';
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';

faker.seed(1); // deterministic field choices

const LOCATIONS = [
  'Main Entrance',
  'Server Room',
  'Parking Garage',
  'R&D Lab',
  'Finance Office',
  'Loading Dock',
];
// Seeded events are auto-logged, never 'manual' (manual is reserved for form-added events).
const SEED_METHODS = ['badge', 'pin', 'mobile'];
const COUNT = 300;

const events = Array.from({ length: COUNT }, (_, i) => {
  const location = faker.helpers.arrayElement(LOCATIONS);
  return {
    id: `evt-${i}`,
    date: faker.date.recent({ days: 60 }),
    title: `Access — ${location}`,
    user: faker.person.fullName(),
    location,
    result: faker.helpers.weightedArrayElement([
      { weight: 85, value: 'granted' },
      { weight: 15, value: 'denied' },
    ]),
    method: faker.helpers.arrayElement(SEED_METHODS),
  };
});

events.sort((a, b) => b.date.getTime() - a.date.getTime());

const rows = events
  .map(
    (e) =>
      `  { id: ${JSON.stringify(e.id)}, date: new Date(${JSON.stringify(
        e.date.toISOString(),
      )}), title: ${JSON.stringify(e.title)}, user: ${JSON.stringify(
        e.user,
      )}, location: ${JSON.stringify(e.location)}, result: ${JSON.stringify(
        e.result,
      )}, method: ${JSON.stringify(e.method)} },`,
  )
  .join('\n');

const file = `// AUTO-GENERATED by scripts/generate-mock-data.mjs — do not edit by hand.
// Regenerate with: npm run generate:data
import type { AccessEvent } from './types';

export const accessEvents: AccessEvent[] = [
${rows}
];
`;

const out = fileURLToPath(new URL('../src/features/events/mockData.ts', import.meta.url));
writeFileSync(out, file);
console.log(`Wrote ${events.length} events to ${out}`);
